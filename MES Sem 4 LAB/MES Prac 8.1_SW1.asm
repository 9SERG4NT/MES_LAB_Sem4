ORG 0000H
LJMP MAIN

ORG 0003H
AJMP ISR_INT0

MAIN:
	
	MOV IE, #81H
	

HERE: SJMP HERE
DELAY:
	MOV R0, #200
DELAY_LOOP:
	DJNZ R0,DELAY_LOOP
	RET
ISR_INT0:
	START:
		SETB P3.3
		SETB P3.4
	
	MOV P1, #11111001B
		CALL DELAY
		CLR P3.3
		MOV P1, #10101011B
		CALL DELAY
		SETB P3.3
		CLR P3.4
		MOV P1, #10000111B
		CALL DELAY
		CLR P3.3
		MOV P1, #11000000B
		CALL DELAY
	JMP START
	CPL P1.0
	RETI
